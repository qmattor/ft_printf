#include "ft_printf.h"

void	print_mods(char *mods)
{
	int x;

	x = 0;
	write(1, "0, #, -, +, l, h, z, j,  ,ll,hh,\n", 33);
	while (x < 11)
		write(1, mods[x++] ? "1, " : "0, ", 4);
}